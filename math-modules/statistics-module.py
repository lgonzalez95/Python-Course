import statistics

print(statistics.mean([1, 2, 3, 4, 5]))
print(statistics.harmonic_mean([1, 2, 3, 4, 5]))
print(statistics.median([1, 2, 4, 5, 6]))
print(statistics.median_high([1, 2, 3, 4, 5, 6]))
print(statistics.median_low([1, 2, 3, 4, 5, 6]))
print(statistics.mode([1, 2, 3, 1, 2, 3, 1, 5, 1, 5, 6, 1]))  # element that appears more times
